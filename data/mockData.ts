import { ABU_DHABI_BOUNDS, ABU_DHABI_AREAS } from "../config/constants"

export const generateMockCameras = (count: number): Camera[] => {
  const cameras: Camera[] = []
  const lastDetectionOptions = ["5m ago", "30m ago", "1h ago", "2h ago", "1d ago"]

  for (let i = 1; i <= count; i++) {
    const lat = ABU_DHABI_BOUNDS.south + Math.random() * (ABU_DHABI_BOUNDS.north - ABU_DHABI_BOUNDS.south)
    const lng = ABU_DHABI_BOUNDS.west + Math.random() * (ABU_DHABI_BOUNDS.east - ABU_DHABI_BOUNDS.west)
    const status = "active"
    const battery = Math.floor(Math.random() * 100)
    const area = ABU_DHABI_AREAS[Math.floor(Math.random() * ABU_DHABI_AREAS.length)]

    cameras.push({
      id: i,
      name: `Camera ${i.toString().padStart(2, "0")}`,
      status,
      lat,
      lng,
      area,
      lastDetection: lastDetectionOptions[Math.floor(Math.random() * lastDetectionOptions.length)],
      battery,
    })
  }
  return cameras
}

// Helper function to get random image for each species with proper cycling
const getSpeciesImage = (animal: string, detectionId: number) => {
  const imageMap = {
    "Arabian Oryx": [
      "/images/arabian-oryx-1.png",
      "/images/arabian-oryx-2.png",
      "/images/arabian-oryx-3.png",
      "/images/arabian-oryx-4.png",
      "/images/arabian-oryx-5.png",
    ],
    "Desert Fox": [
      "/images/desert-fox-1.png",
      "/images/desert-fox-2.png",
      "/images/desert-fox-3.png",
      "/images/desert-fox-4.png",
      "/images/desert-fox-5.png",
    ],
    "Sand Cat": [
      "/images/sand-cat-1.png",
      "/images/sand-cat-2.png",
      "/images/sand-cat-3.png",
      "/images/sand-cat-4.png",
      "/images/sand-cat-5.png",
    ],
  }

  const images = imageMap[animal as keyof typeof imageMap]
  if (!images) {
    return "/placeholder.svg?height=60&width=60&text=Unknown+Species"
  }

  // Use detection ID to cycle through images consistently
  const imageIndex = (detectionId - 1) % images.length
  return images[imageIndex]
}

